require_relative 'spec_helper.rb'

RSpec.describe 'グロービス学び放題' do
  it '動画が再生できること。' do
    puts('グロービス学び放題を開く')
    puts('グロービス学び放題で、ログインする')
    puts('動画画面に遷移する。')
    puts('動画を視聴する。')
    puts('動画が再生されること。')
  end
  it 'いいねできること' do
    puts('グロービス学び放題を開く')
    puts('グロービス学び放題で、ログインする')
    puts('動画画面に遷移する。')
    puts('いいねボタンを押せること。')
    puts('いいねボタンが押せていること。')
  end
  it 'いいねできること' do
    puts('グロービス学び放題を開く')
    puts('グロービス学び放題で、ログインする')
    puts('達人の一冊を開く')
    puts('加藤さんの動画を再生する。')
    puts('アンドロイドは電気羊の夢をみるかの文字を発見する・')
  end
end
